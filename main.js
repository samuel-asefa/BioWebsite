        // Application Logic
        class BiologyStudyApp {
            constructor() {
                this.modal = document.getElementById('modal');
                this.modalCloseButton = document.getElementById('modalClose');
                this.modalBody = document.getElementById('modal-body');

                this.init();
            }

            init() {
                this.createParticles();
                this.renderTopics();
                this.addModalListeners();
            }

            createParticles() {
                const particlesContainer = document.getElementById('particles');
                const particleCount = 50;

                for (let i = 0; i < particleCount; i++) {
                    const particle = document.createElement('div');
                    particle.className = 'particle';
                    
                    const size = Math.random() * 4 + 2;
                    const initialX = Math.random() * window.innerWidth;
                    const animationDuration = Math.random() * 20 + 10;
                    const delay = Math.random() * 20;

                    particle.style.cssText = `
                        width: ${size}px;
                        height: ${size}px;
                        left: ${initialX}px;
                        animation-duration: ${animationDuration}s;
                        animation-delay: ${delay}s;
                    `;

                    particlesContainer.appendChild(particle);
                }
            }

            renderTopics() {
                const topicsGrid = document.getElementById('topicsGrid');
                
                studyTopics.forEach((topic, index) => {
                    const topicCard = this.createTopicCard(topic, index);
                    topicsGrid.appendChild(topicCard);
                });
            }

            createTopicCard(topic, index) {
                const card = document.createElement('div');
                card.className = 'topic-card fade-in';
                card.style.animationDelay = `${index * 0.1}s`;
                card.innerHTML = `
                    <div class="topic-header" data-index="${index}">
                        <h3 class="topic-title">${topic.title}</h3>
                        <div class="topic-meta">
                            <span class="meta-badge">
                                <span>üìö</span>
                                ${topic.difficulty}
                            </span>
                            <span class="meta-badge">
                                <span>‚è±Ô∏è</span>
                                ${topic.duration}
                            </span>
                        </div>
                    </div>
                `;

                // Add event listener to open the modal
                card.querySelector('.topic-header').addEventListener('click', () => {
                    this.showModal(topic);
                });

                return card;
            }

            showModal(topic) {
                this.modalBody.innerHTML = `
                    <h2 class="modal-title">${topic.title}</h2>
                    <div class="content-section">
                        <h3>Key Concepts</h3>
                        <ul class="concept-list">
                            ${topic.concepts.map(concept => `
                                <li class="concept-item">
                                    <div class="concept-term">${concept.term}</div>
                                    <div class="concept-definition">${concept.definition}</div>
                                    <div class="concept-example"><strong>Example:</strong> ${concept.example}</div>
                                </li>
                            `).join('')}
                        </ul>
                    </div>
                    <div class="quiz-section">
                        <h3>Practice Questions</h3>
                        <div class="quiz-cards">
                            ${topic.quiz.map((q, i) => `
                                <div class="quiz-card" data-question="${i}">
                                    <div class="quiz-card-inner">
                                        <div class="quiz-card-front">
                                            <p><strong>Question:</strong> ${q.question}</p>
                                            <p style="margin-top: 1rem; font-size: 0.9rem; opacity: 0.8;">Click to reveal answer</p>
                                        </div>
                                        <div class="quiz-card-back">
                                            <p><strong>Answer:</strong> ${q.answer}</p>
                                        </div>
                                    </div>
                                </div>
                            `).join('')}
                        </div>
                    </div>
                `;
                
                // Add event listeners to the new quiz cards in the modal
                this.modalBody.querySelectorAll('.quiz-card').forEach(quizCard => {
                    quizCard.addEventListener('click', (e) => {
                        this.flipQuizCard(quizCard);
                    });
                });
                
                this.modal.classList.add('visible');
            }

            closeModal() {
                this.modal.classList.remove('visible');
                // Clear the content to prevent flashing on next open
                this.modalBody.innerHTML = ''; 
            }

            addModalListeners() {
                this.modalCloseButton.addEventListener('click', () => this.closeModal());
                this.modal.addEventListener('click', (e) => {
                    if (e.target === this.modal) {
                        this.closeModal();
                    }
                });
            }

            flipQuizCard(quizCard) {
                quizCard.classList.toggle('flipped');
            }
        }

        // Initialize the application when DOM is loaded
        document.addEventListener('DOMContentLoaded', () => {
            new BiologyStudyApp();
        });

        // Smooth scrolling for anchor links
        document.querySelectorAll('a[href^="#"]').forEach(anchor => {
            anchor.addEventListener('click', function (e) {
                e.preventDefault();
                const target = document.querySelector(this.getAttribute('href'));
                if (target) {
                    target.scrollIntoView({
                        behavior: 'smooth',
                        block: 'start'
                    });
                }
            });
        });
